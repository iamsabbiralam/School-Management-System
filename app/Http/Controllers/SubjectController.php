     * Store a newly created resource in storage.
     *
     * @param  App\Http\Request\SubjectRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(SubjectRequest $request)
    {
        $subject = Subject::create([
            'subjects' => $request->get('subjects'),
        ]);

        if(empty($subject)) {
            return redirect()->back()->withInput()->with("ERROR", __("Failed to create subject"));
        }
        return redirect()->route('subjects.index')->with("SUCCESS", __("Subject has been created successfully"));
    }

